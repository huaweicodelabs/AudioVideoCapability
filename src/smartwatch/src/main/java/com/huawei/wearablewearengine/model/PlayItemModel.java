package com.huawei.wearablewearengine.model;

import java.io.Serializable;

public class PlayItemModel implements Serializable {

    private int imageId;
    private String title;
    private String thumbnailUrl;
    private String songUrl;
    private String album;
    private String singer;
    private String category;
    private String duration;
    private String mode;
    private boolean isChecked;

    public PlayItemModel() {
    }

    public PlayItemModel(int imageId, String title, String thumbnailUrl, String songUrl, String album, String singer, String category, String duration, String mode) {
        this.title = title;
        this.thumbnailUrl = thumbnailUrl;
        this.songUrl = songUrl;
        this.album = album;
        this.singer = singer;
        this.category = category;
        this.duration = duration;
        this.mode = mode;
    }

    public boolean isChecked() {
        return isChecked;
    }

    public void setChecked(boolean checked) {
        isChecked = checked;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getThumbnailUrl() {
        return thumbnailUrl;
    }

    public void setThumbnailUrl(String thumbnailUrl) {
        this.thumbnailUrl = thumbnailUrl;
    }

    public String getSongUrl() {
        return songUrl;
    }

    public void setSongUrl(String songUrl) {
        this.songUrl = songUrl;
    }

    public String getAlbum() {
        return album;
    }

    public void setAlbum(String album) {
        this.album = album;
    }

    public String getSinger() {
        return singer;
    }

    public void setSinger(String singer) {
        this.singer = singer;
    }

    public String getCategory() {
        return category;
    }

    public void setCategory(String category) {
        this.category = category;
    }

    public String getDuration() {
        return duration;
    }

    public void setDuration(String duration) {
        this.duration = duration;
    }

    public String getMode() {
        return mode;
    }

    public void setMode(String mode) {
        this.mode = mode;
    }

    public int getImageId() {
        return imageId;
    }

    public void setImageId(int imageId) {
        this.imageId = imageId;
    }
}
